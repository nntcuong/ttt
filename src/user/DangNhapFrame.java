    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        if (kiemTraKhongRong()) {
            String email = jTextField1.getText();
            String matkhau = String.valueOf(jPasswordField1.getPassword());
            if (jRadioButtonQuanTriVien.isSelected()) {
                
                try {
                    Connection connection = JDBCUtil.getConnection();
                    String sql = "SELECT * FROM quantrivien WHERE email=? AND matkhau=?";
                    PreparedStatement statement = connection.prepareStatement(sql);
                    statement.setString(1, email);
                    statement.setString(2, matkhau);
                    ResultSet resultSet = statement.executeQuery();

                    if (resultSet.next()) {
                        GiaoDienQuanLyFrame nd = new GiaoDienQuanLyFrame();
                        nd.setVisible(true);
                        nd.pack();
                        GiaoDienQuanLyFrame.jLabelEmail.setText(email);
                        this.dispose();
                    } else {
                        JOptionPane.showMessageDialog(this, "Tài khoản hoặc mật khẩu không đúng");
                    }

                } catch (SQLException ex) {
                    Logger.getLogger(DangNhapFrame.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            else if (jRadioButtonNhaCungCap.isSelected()) {
                
                try {
                    Connection connection = JDBCUtil.getConnection();
                    String sql = "SELECT * FROM nhacungcap WHERE email=? AND matkhau=?";
                    PreparedStatement statement = connection.prepareStatement(sql);
                    statement.setString(1, email);
                    statement.setString(2, matkhau);
                    ResultSet resultSet = statement.executeQuery();

                    if (resultSet.next()) {
                        GiaoDienQuanLyNhaCungCapFrame ql = new GiaoDienQuanLyNhaCungCapFrame();
                        ql.setVisible(true);
                        ql.pack();
                        GiaoDienQuanLyNhaCungCapFrame.jLabelEmail.setText(email);
                        this.dispose();
                    } else {
                        JOptionPane.showMessageDialog(this, "Tài khoản hoặc mật khẩu không đúng");
                    }

                } catch (SQLException ex) {
                    Logger.getLogger(DangNhapFrame.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
                else if (jRadioButtonKhachHang.isSelected()) {
                
                try {
                    Connection connection = JDBCUtil.getConnection();
                    String sql = "SELECT * FROM khachhang WHERE email=? AND matkhau=?";
                    PreparedStatement statement = connection.prepareStatement(sql);
                    statement.setString(1, email);
                    statement.setString(2, matkhau);
                    ResultSet resultSet = statement.executeQuery();

                    if (resultSet.next()) {
                        GiaoDienNguoiDungFrame ql = new GiaoDienNguoiDungFrame();
                        ql.setVisible(true);
                        ql.pack();
                        GiaoDienNguoiDungFrame.jLabelEmail.setText(email);
                        this.dispose();
                    } else {
                        JOptionPane.showMessageDialog(this, "Tài khoản hoặc mật khẩu không đúng");
                    }

                } catch (SQLException ex) {
                    Logger.getLogger(DangNhapFrame.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_jButton1MouseClicked

