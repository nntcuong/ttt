    private  boolean kiemTraKhongRong(){
    
        if(jTextField1.getText().isEmpty()){
        
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập tài khoản ", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if(String.valueOf(jPasswordField1.getPassword()).isEmpty()){
        
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập mật khẩu ", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }
