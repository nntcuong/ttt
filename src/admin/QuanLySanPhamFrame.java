    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(kiemTraKhongRong()){
        
            int id=Integer.parseInt(jTextFieldID.getText());
            String tensanpham=jTextFieldTen.getText();
            String danhmuc = jComboBoxDanhMuc.getSelectedItem().toString();
            String nhacungcap = jComboBoxNhaCungCap.getSelectedItem().toString();
            int id_dm=dmDAO.getID(jComboBoxDanhMuc.getSelectedItem().toString());
            int id_ncc=nccDAO.getId(jComboBoxNhaCungCap.getSelectedItem().toString());
            
            if (kiemTraGia(jTextFieldGia.getText())) {
                double gia = Double.parseDouble(jTextFieldGia.getText());
                if (kiemTraSoLuong(jTextFieldSoLuong.getText())) {

                    int soluong = Integer.parseInt(jTextFieldSoLuong.getText());
                    if (!spDAO.checkID(id)) {
                        SanPham sp = new SanPham(id, tensanpham, danhmuc, nhacungcap, gia, soluong,id_dm,id_ncc);
                        spDAO.insert(sp);
                        QuanLySanPhamFrame spF = new QuanLySanPhamFrame();
                        spF.setVisible(true);
                        spF.pack();
                        this.dispose();
                    } else {
                        JOptionPane.showMessageDialog(this, "Mã đã tồn tại", "Thông báo", 2);
                    }
                }

            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

