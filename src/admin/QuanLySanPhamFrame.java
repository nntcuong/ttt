    private  boolean kiemTraKhongRong(){
    
        if(jTextFieldTen.getText().isEmpty()){
        
            JOptionPane.showMessageDialog(this, "Nhập tên sản phẩm !!!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if(jComboBoxDanhMuc.getSelectedItem()==null){
        
            JOptionPane.showMessageDialog(this, "Chọn danh mục !!!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
            
        }
        if(jComboBoxNhaCungCap.getSelectedItem()==null){
        
            JOptionPane.showMessageDialog(this, "Chọn danh mục !!!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        
        }if(jTextFieldSoLuong.getText().isEmpty()){
        
            JOptionPane.showMessageDialog(this, "Nhập số lượng !!!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if(jTextFieldGia.getText().isEmpty()){
        
            JOptionPane.showMessageDialog(this, "Nhập giá !!!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }
       
        
        return true;
    }
